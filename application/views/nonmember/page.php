                    <h5>Dilapidations</h5>
                    <p>Commercial property in all sectors, industrial, commercial and retail, where there is a lease involved, is subject to the law of dilapidations. We at Johnson Gillies have considerable experience in the practice of assessing dilapidations and are often requested to assess a dilapidation liability on behalf of a landlord or a tenant, to enable a decision to be made as to a financial liability in the future. This can often be incorporated within the business accounts.</p>
                    <p>On behalf of landlords we are frequently requested to inspect and prepare a claim against tenants either on an interim basis mid-term in a lease or as a terminal schedule at the end of the lease. We have considerable experience in preparing and negotiating settlements.</p>
                    <p>On behalf of lessee when served with a schedule of dilapidation there are a number of defences available to minimise the tenant&#8217;s obligations. Landlords frequently claim a higher level of breach of obligation and it is our role to prove and advise on a level of settlement.</p>
